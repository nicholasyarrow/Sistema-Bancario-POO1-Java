    // Transferencia entre cuentas creadas en la misma aplicacion
    private void btnTransferenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransferenciaActionPerformed
        // Obtener las cuentas involucradas en la transferencia
        CuentaBancaria cuentaOrigen = SessionManager.obtenerCuentaUsuarioActual();
        String cuentaDestinoNumero = txtCuentaDestino.getText(); // Obtener el n√∫mero de cuenta destino

        // Buscar las cuentas en la lista
        CuentaBancaria cuentaDestino = null;
        for (CuentaBancaria cuenta : SessionManager.getCuentas()) {
            if (cuenta.getUsuario().equals(cuentaDestinoNumero)) {
                cuentaDestino = cuenta;
                break;
            }
        }

        // Verificar que las cuentas existan
        if (cuentaOrigen != null && cuentaDestino != null) {
            // Obtener el monto a transferir
            double monto = Double.parseDouble(txtMontoTransferencia.getText());

            // Verificar si la cuenta origen tiene saldo suficiente
            if (cuentaOrigen.getBalance() >= monto) {
                // Realizar la transferencia
                cuentaOrigen.retirar(monto);
                double balanceActualizado = cuentaOrigen.getBalance();
                // Actualizar los saldos en el archivo cuentas.txt 
                // Actualizar la informaci√≥n en el archivo y en la lista de cuentas en SessionManager
                // Guardar cambios en el archivo
                cuentaOrigen.setBalance(balanceActualizado);
                actualizarArchivoCuenta(cuentaOrigen);
                cuentaDestino.depositar(monto);
                double balanceActualizado2 = cuentaDestino.getBalance();
                // Actualizar los saldos en el archivo cuentas.txt
                // Actualizar la informaci√≥n en el archivo y en la lista de cuentas en SessionManager
                // Guardar cambios en el archivo
                cuentaDestino.setBalance(balanceActualizado2);
                actualizarArchivoCuenta(cuentaDestino);

                // Actualizar la ventana actual con los nuevos saldos
                lblBalanceUsuario.setText(String.valueOf(cuentaOrigen.getBalance()));
                // Actualizar tambi√©n el saldo de la cuenta destino si est√°s mostrando esa informaci√≥n
                
                // Registrar la transacci√≥n de cuenta origen
                RegistroTransacciones.guardarTransaccion(cuentaOrigen.getUsuario(), "Transferencia", monto);
                // Registrar la transacci√≥n de cuenta destino
                RegistroTransacciones.guardarTransaccion(cuentaDestino.getUsuario(), "Transferencia", monto);
            } else {
                // Mostrar un mensaje si la cuenta origen no tiene saldo suficiente
                lblMensajeError.setText("Saldo insuficiente para la transferencia");
            }
        } else {
            // Mostrar un mensaje si alguna de las cuentas no existe
            lblMensajeError.setText("Una o ambas cuentas no existen");
        }
        OperacionesUsuario operaciones = new OperacionesUsuario();
        operaciones.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnTransferenciaActionPerformed
    // M√©todo para actualizar el archivo de cuentas con la informaci√≥n actualizada
    private void actualizarArchivoCuenta(CuentaBancaria cuentaActualizada) {
        try (BufferedReader reader = new BufferedReader(new FileReader("cuentas.txt"))) {
            String linea;
            ArrayList<String> lineas = new ArrayList<>();

            while ((linea = reader.readLine()) != null) {
                if (linea.contains(cuentaActualizada.getUsuario())) {
                    linea = cuentaActualizada.aFormatoArchivo(); // Actualizar la l√≠nea con la cuenta actualizada
                }
                lineas.add(linea);
            }

            // Escribir todas las l√≠neas actualizadas en el archivo
            try (PrintWriter writer = new PrintWriter(new FileWriter("cuentas.txt"))) {
                for (String updatedLine : lineas) {
                    writer.println(updatedLine);
                }
                
                // Actualizar la lista de cuentas en SessionManager despu√©s de modificar el archivo
                SessionManager.cargarCuentasDesdeArchivo();
            }
        } catch (IOException e) {
            e.printStackTrace(); // Manejo de excepciones al escribir en el archivo
        }
    }
