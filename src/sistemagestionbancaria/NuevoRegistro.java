    // Creacion de nueva cuenta bancaria
    private void btnNuevoRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoRegistroActionPerformed
        // Obtener los valores de los campos de texto
            String nombre = txtNombre.getText();
            String apellidos = txtApellido.getText();
            String dni = txtDNI.getText();
            String celular = txtCelular.getText();
            String direccion = txtDireccion.getText();
            String usuario = txtUsuario.getText();
            
            // Obtener los PIN desde JPasswordField y compararlos como String
            char[] pinChars = txtPIN.getPassword();
            char[] confirmarPinChars = txtPin.getPassword();
            String pin = new String(pinChars);
            String confirmarPin = new String(confirmarPinChars);
            // Validar si los PIN tienen cuatro d√≠gitos exactamente
            if (pin.length() != 4 || confirmarPin.length() != 4){
                JOptionPane.showMessageDialog(this, "El PIN debe contener 4 d√≠gitos.", "PIN inv√°lido", JOptionPane.ERROR_MESSAGE);
                // Limpiar los campos de PIN para volver a solicitarlos
                txtPIN.setText("");
                txtPin.setText("");
            }
            //Verificar si los PIN consisten de digitos
            else if (!pin.matches("\\d{4}")||!confirmarPin.matches("\\d{4}")) {
                JOptionPane.showMessageDialog(this, "El PIN debe contener solo d√≠gitos.", "PIN inv√°lido", JOptionPane.ERROR_MESSAGE);
                // Limpiar los campos de PIN para volver a solicitarlos
                txtPIN.setText("");
                txtPin.setText("");
            }
            // Verificar si los PIN coinciden 
            else if (!pin.equals(confirmarPin)) {
                JOptionPane.showMessageDialog(this, "Los PIN ingresados no coinciden. Por favor, intenta de nuevo.", "PIN inv√°lido", JOptionPane.ERROR_MESSAGE);
                // Limpiar los campos de PIN para volver a solicitarlos
                txtPIN.setText("");
                txtPin.setText("");
            } else {
                // Crear una nueva cuenta bancaria con los datos obtenidos
                CuentaBancaria nuevaCuenta = new CuentaBancaria(nombre, apellidos, dni, celular, direccion, usuario, pin, 0, 0);

                // Agregar la nueva cuenta a la lista de cuentas solo si los pines coinciden
                SessionManager.getCuentas().add(nuevaCuenta);
                // Guardar cuentas en el archivo
                try (BufferedWriter writer = new BufferedWriter(new FileWriter("cuentas.txt"))) {
                    for (CuentaBancaria cuenta : SessionManager.getCuentas()) {
                        writer.write(cuenta.aFormatoArchivo());
                        writer.newLine();
                    }
                    writer.close();
                } catch (IOException ex) {
                    ex.printStackTrace(); // Manejo de excepciones
                }
                SessionManager.setUsuarioActual(usuario); 
                
                // Abrir nueva ventana de operaciones para el usuario
                OperacionesUsuario operaciones = new OperacionesUsuario();
                operaciones.setVisible(true); // Muestra la ventana de operaciones de usuario
                dispose(); //cierra ventana actual
            }
    }//GEN-LAST:event_btnNuevoRegistroActionPerformed

