    private void btnImprimirReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirReporteActionPerformed
        try {
            // Obtener el nombre del usuario actual desde la cuenta bancaria
            CuentaBancaria cuenta = SessionManager.obtenerCuentaUsuarioActual();
            String nombreUsuario = cuenta.getUsuario();

            // Nombre predeterminado del archivo basado en el nombre de usuario
            String nombrePredeterminado = "ReporteBankUsil" + nombreUsuario + ".txt";

            // Mostrar un di√°logo de selecci√≥n de archivo con el nombre predeterminado
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Guardar Reporte");
            fileChooser.setSelectedFile(new File(nombrePredeterminado));

            // Filtro para mostrar solo archivos de texto
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de texto (*.txt)", "txt");
            fileChooser.setFileFilter(filter);

            // Mostrar el di√°logo de guardado
            int userSelection = fileChooser.showSaveDialog(this);

            if (userSelection == JFileChooser.APPROVE_OPTION) {
                File fileToSave = fileChooser.getSelectedFile();

                // Verificar si el archivo ya existe
                if (fileToSave.exists()) {
                    int result = JOptionPane.showConfirmDialog(this, "El archivo ya existe. ¬øDeseas reemplazarlo?", "Archivo existente", JOptionPane.YES_NO_OPTION);
                    if (result != JOptionPane.YES_OPTION) {
                        return;
                    }
                }

                // Verificar si el archivo seleccionado termina con ".txt"
                if (!fileToSave.getName().endsWith(".txt")) {
                    fileToSave = new File(fileToSave.getAbsolutePath() + ".txt");
                }

                // Escribir el contenido del JTextArea en el archivo seleccionado
                try (PrintWriter writer = new PrintWriter(fileToSave)) {
                    writer.println(txtAreaTransacciones.getText());
                }

                JOptionPane.showMessageDialog(this, "Reporte exportado exitosamente.");
            }
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al exportar el reporte: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        // Abrir nueva ventana de operaciones para el usuario
        OperacionesUsuario operaciones = new OperacionesUsuario();
        operaciones.setVisible(true); // Muestra la ventana de operaciones de usuario
        dispose(); //cierra ventana actual
    }//GEN-LAST:event_btnImprimirReporteActionPerformed

