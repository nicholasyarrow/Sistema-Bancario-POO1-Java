    // Muestra el reporte de transacciones pertenecientes al usuario actual
    private void mostrarTransaccionesUsuarioActual() {
        // Obtener el usuario actual desde SessionManager
        CuentaBancaria cuenta = SessionManager.obtenerCuentaUsuarioActual();
        String usuarioActual = cuenta.getUsuario(); // Obtener el usuario actual
        
        // Obtener las transacciones del usuario actual
        List<String> transacciones = obtenerTransaccionesPorUsuario(usuarioActual);

        // Mostrar las transacciones en el JTextArea
        StringBuilder transaccionesText = new StringBuilder();
        for (String transaccion : transacciones) {
            transaccionesText.append(transaccion).append("\n");
        }
        txtAreaTransacciones.setText(transaccionesText.toString());
    }
     private List<String> obtenerTransaccionesPorUsuario(String usuario) {
        List<String> transacciones = new ArrayList<>();
        try (BufferedReader reader = new BufferedReader(new FileReader("transacciones.txt"))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] partes = linea.split(",");
                String usuarioRegistro = partes[0];
                String tipo = partes[1];
                double monto = Double.parseDouble(partes[2]);

                if (usuarioRegistro.equals(usuario)) {
                    String transaccion = "Usuario: " + usuarioRegistro + ", Tipo: " + tipo + ", Monto: " + monto;
                    transacciones.add(transaccion);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return transacciones;
    }

